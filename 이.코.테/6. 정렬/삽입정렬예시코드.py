# 삽입 정렬
# 인덱스 0(7)은 이미 정렬되어 있는 영역이라고 가정하고 여기에 끼워 맞춰서 정렬하는 방법
arr = [7,5,9,0,3,1,6,2,4,8]

for i in range(1,len(arr)):
    for j in range(i,0,-1):
        if arr[j] < arr[j-1]:
            arr[j], arr[j-1] = arr[j-1], arr[j]
        else:
            break
        print(arr)
